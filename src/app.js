export class Main {
    ClockSingleMinute(minutes){
        if(minutes === 1 ){
            return "YOOO";
        }

        if(minutes === 2 ){
            return "YYOO";
        }
        
        if(minutes === 3){
            return "YYYO";
        }

        if(minutes === 4){
            return "YYYY";
        }

    
    }
}